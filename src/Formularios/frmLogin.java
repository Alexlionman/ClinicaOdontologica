        if (r.loginRecepcionista(txtLogin.getText(), txtSenha.getText())) {
            frmPrincipal fp = new frmPrincipal();
            fp.setVisible(true);
            fp.usuarioLogado(txtLogin.getText());
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Login ou senha invalidos!");
            
        }
